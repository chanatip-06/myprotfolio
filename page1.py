# ภารกิจ: พลิกโค้ดสี่เหลี่ยมมุมฉาก -> โปรแกรมช่วยคำนวณกระเบื้องปูพื้น
# ===================================================

print("=== โปรแกรมคำนวณพื้นที่ห้อง และประเมินจำนวนกระเบื้อง ===")

# 1. รับค่าขนาดห้องจากผู้ใช้งาน (รองรับทศนิยม)
กว้าง = float(input("กรอกความกว้างของห้อง (เมตร): "))
ยาว = float(input("กรอกความยาวของห้อง (เมตร): "))

# 2. คำนวณพื้นที่ห้อง (ต่อยอดจากโค้ดต้นแบบของครู)
พื้นที่ห้อง = กว้าง * ยาว

# 3. ใส่ฟีเจอร์เพิ่ม: ให้เลือกขนาดกระเบื้องเพื่อคำนวณจำนวนแผ่น
print("\n--- เลือกขนาดกระเบื้องที่ต้องการใช้ ---")
print("1. แผ่นเล็ก (30x30 ซม.) -> 1 ตารางเมตรใช้ประมาณ 11 แผ่น")
print("2. แผ่นใหญ่ (60x60 ซม.) -> 1 ตารางเมตรใช้ประมาณ 3 แผ่น")
ตัวเลือก = input("เลือกขนาดกระเบื้อง (กด 1 หรือ 2): ")

# 4. ใช้เงื่อนไข (If-Else) ในการคำนวณจำนวนกระเบื้อง
if ตัวเลือก == "1":
    จำนวนกระเบื้องที่ต้องใช้ = พื้นที่ห้อง * 11
    ขนาดกระเบื้อง = "30x30 ซม."
elif ตัวเลือก == "2":
    จำนวนกระเบื้องที่ต้องใช้ = พื้นที่ห้อง * 3
    ขนาดกระเบื้อง = "60x60 ซม."
else:
    # เผื่อกรณีเลือกผิด จะคิดค่าเฉลี่ยกลางๆ ให้
    จำนวนกระเบื้องที่ต้องใช้ = พื้นที่ห้อง * 6 
    ขนาดกระเบื้อง = "ขนาดมาตรฐาน (ทั่วไป)"

# 5. แสดงผลลัพธ์แบบเข้าใจง่าย
print("\n================ สรุปผลการคำนวณ ================")
print(f"📐 พื้นที่ห้องของคุณคือ: {พื้นที่ห้อง:.2f} ตารางเมตร")
print(f"🧱 หากเลือกใช้กระเบื้องขนาด: {ขนาดกระเบื้อง}")
print(f"🛒 คุณจะต้องซื้อกระเบื้องทั้งหมดประมาณ: {จำนวนกระเบื้องที่ต้องใช้:.0f} แผ่น")
print("=================================================")
print("💡 แนะนำ: ควรซื้อเผื่อเหลือเผื่อขาดไว้ประมาณ 5-10% นะครับ")